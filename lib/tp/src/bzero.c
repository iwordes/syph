#include <libtp_.h>

void	tp__bzero(void *mem_, size_t n)
{
	char	*mem;
	size_t	i;

	i = 0;
	mem = mem_;
	while (n - i >= 8)
	{
		*((long*)(mem + i)) = 0;
		i += 8;
	}
	while (i < n)
		mem[i++] = 0;
}
